var btnModulo1 = document.querySelector("#btn-modulo1");
var conteudoModulo1 = document.querySelector('#conteudo-oculto1');

btnModulo1.addEventListener('click', function() {
    
    if(conteudoModulo1.style.display  === "block"){
        conteudoModulo1.style.display = "none";
        btnModulo1.style.rotate = "0deg";
       
    } else{
        conteudoModulo1.style.display = "block";
        btnModulo1.style.rotate = "90deg";
    }

}); 



